public class ohm {
    public static void main(String[] args) {
        double V; // Tegangan dalam volt
        double I; // Arus dalam ampere
        double R; // Resistansi dalam ohm

        // Menggunakan nilai-nilai contoh
        I = 2.0; // Arus dalam ampere
        R = 5.0; // Resistansi dalam ohm

        // Menghitung tegangan menggunakan rumus Ohm
        V = I * R;

        // Menampilkan hasil
        System.out.println("Arus (I) dalam ampere: " + I);
        System.out.println("Resistansi (R) dalam ohm: " + R);
        System.out.println("Tegangan (V) dalam volt: " + V);
    }
}
    

